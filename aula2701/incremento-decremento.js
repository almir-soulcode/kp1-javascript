// Incremento -> Somar de 1 em 1
// Decremento -> Subtrair de 1 em 1

let a = 0;
a++; // a = 0 + 1
a++; // a = 1 + 1
a++; // a = 2 + 1
a++; // a = 3 + 1
console.log(a);
a--; // a = 4 - 1
console.log(a);

// Para outros valores
let idade = 18;
idade += 5; // idade = idade + 5
idade -= 2; // idade = idade - 2
console.log("Idade: ", idade);