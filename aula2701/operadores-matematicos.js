// Operadores matemáticos => fazer cálculos
    // Soma (+)
    // Subtração (-)
    // Divisão (/)
    // Multiplicação (*)
    // Resto (%)
    // Exponenciação (**)

let num1 = 5, num2 = 8, num3 = 4.5; // várias declarações na mesma linha
let resultado;

resultado = num1 + num2;
console.log(resultado);

resultado = num1 - num2;
console.log(resultado);

resultado = num1 * 5;
console.log(resultado);

resultado = num2 / 3;
console.log(resultado);

// Expressão matemática
// Mesmas regras das expressões (parênteses dá prioridade)
resultado = (num1 + num2) * num3 / 3;
console.log(resultado);

// Exponenciação
resultado = num1 ** 4; // 5 elevado a 4 = 5 * 5 * 5 * 5
console.log(resultado);

// Resto
console.log(10 % 3);
console.log(10 % 2);
console.log(11 % 2);